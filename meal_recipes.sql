CREATE INDEX idx_ingredients_name ON ingredients(name);
CREATE INDEX idx_ingredients_medicinal ON ingredients(is_medicinal);

-- 常用查询视图: 食谱概览
CREATE VIEW v_recipe_overview AS
SELECT
    r.id AS recipe_id,
    reg.name AS region,
    s.name  AS season,
    r.energy_kcal,
    r.title,
    r.oil_g,
    r.salt_note,
    r.protein_g, r.carb_g, r.fat_g,
    r.protein_pct, r.carb_pct, r.fat_pct
FROM recipes r
JOIN regions reg ON reg.id = r.region_id
JOIN seasons s   ON s.id  = r.season_id;

-- 常用查询视图: 完整菜品明细
CREATE VIEW v_dish_detail AS
SELECT
    reg.name AS region,
    s.name   AS season,
    r.energy_kcal,
    CASE m.meal_type
        WHEN 'breakfast' THEN '早餐'
        WHEN 'snack'     THEN '加餐'
        WHEN 'lunch'     THEN '中餐'
        WHEN 'dinner'    THEN '晚餐'
    END AS meal_type,
    d.name AS dish_name,
    i.name AS ingredient_name,
    i.amount, i.unit, i.state,
    CASE i.is_medicinal WHEN 1 THEN '是' ELSE '否' END AS is_medicinal
FROM recipes r
JOIN regions reg  ON reg.id = r.region_id
JOIN seasons s    ON s.id  = r.season_id
JOIN meals m      ON m.recipe_id = r.id
JOIN dishes d     ON d.meal_id = m.id
JOIN ingredients i ON i.dish_id = d.id
ORDER BY r.id, m.sort_order, d.sort_order, i.sort_order;

