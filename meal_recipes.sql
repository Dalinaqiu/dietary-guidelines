CREATE INDEX idx_recipes_region ON recipes(region_id);
CREATE INDEX idx_recipes_season ON recipes(season_id);
CREATE INDEX idx_recipes_energy ON recipes(energy_kcal);
CREATE INDEX idx_meals_recipe ON meals(recipe_id);
